// â”€â”€ 1. Connect to MongoDB (uses config/database.js with validation) â”€â”€â”€â”€â”€â”€â”€â”€â”€
connectDB();

// â”€â”€ 2. View Engine (EJS) â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
app.set('view engine', 'ejs');
